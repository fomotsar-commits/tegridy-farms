#!/usr/bin/env node
/**
 * extract-missing-abis.mjs — Pull the 8 contracts missing from contracts.ts
 * (per AUDIT_FINDINGS H1) out of the forge build artifacts and write them
 * to frontend/src/lib/abi-supplement.ts.
 *
 * Prerequisites:
 *   cd contracts && forge build
 *
 * Usage:
 *   node scripts/extract-missing-abis.mjs
 *
 * After running, add one line to frontend/src/lib/contracts.ts:
 *   export * from './abi-supplement';
 */

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
// Repo layout: contracts/ and frontend/ are siblings at the top level.
// Script lives at frontend/scripts/, so the repo root is two levels up.
const REPO_ROOT = join(__dirname, '..', '..');
const ARTIFACT_DIR = join(REPO_ROOT, 'contracts', 'out');
const OUTPUT_FILE = join(REPO_ROOT, 'frontend', 'src', 'lib', 'abi-supplement.ts');

// Map: contract source filename -> export constant name.
// These are the 8 flagged as missing in AUDIT_FINDINGS.md H1.
// Note: TEGRIDY_DROP_ABI and TEGRIDY_NFT_POOL_ABI already exist as hand-written
// partial ABIs in contracts.ts. Suffixed with _FULL here to avoid export
// collision.
//
// 2026-04-26 sprint update: added TegridyStakingAdmin + SwapFeeRouterAdmin —
// sister contracts created during the EIP-170 splits (commits 99eaf9b +
// cb3d12b). The dApp MUST use these for propose/execute/cancel admin calls
// because those functions moved off the parent contracts.
const MISSING = [
  { sol: 'POLAccumulator.sol',        name: 'POL_ACCUMULATOR_ABI' },
  // TegridyDrop.sol (v1) was deleted in the V1 cleanup; only V2 ships now.
  // The full V2 ABI is generated from TegridyDropV2.sol below.
  { sol: 'TegridyDropV2.sol',         name: 'TEGRIDY_DROP_V2_ABI_FULL' },
  { sol: 'TegridyFeeHook.sol',        name: 'TEGRIDY_FEE_HOOK_ABI' },
  { sol: 'TegridyLPFarming.sol',      name: 'TEGRIDY_LP_FARMING_ABI' },
  { sol: 'TegridyNFTPool.sol',        name: 'TEGRIDY_NFT_POOL_ABI_FULL' },
  { sol: 'TegridyPair.sol',           name: 'TEGRIDY_PAIR_ABI' },
  { sol: 'TegridyTWAP.sol',           name: 'TEGRIDY_TWAP_ABI' },
  { sol: 'TegridyTokenURIReader.sol', name: 'TEGRIDY_TOKEN_URI_READER_ABI' },
  // 2026-04-26 sprint additions: post-EIP-170-split admin sister contracts
  { sol: 'TegridyStakingAdmin.sol',   name: 'TEGRIDY_STAKING_ADMIN_ABI' },
  { sol: 'SwapFeeRouterAdmin.sol',    name: 'SWAP_FEE_ROUTER_ADMIN_ABI' },
];

if (!existsSync(ARTIFACT_DIR)) {
  console.error(`Missing ${ARTIFACT_DIR}. Run: cd contracts && forge build`);
  process.exit(1);
}

const lines = [
  '/**',
  ' * abi-supplement.ts — Auto-generated by scripts/extract-missing-abis.mjs',
  ' *',
  ' * ABIs for the 8 contracts that were missing from contracts.ts per',
  ' * AUDIT_FINDINGS.md H1. Re-run the script whenever you change one of the',
  ' * underlying Solidity contracts.',
  ' *',
  ' * Do NOT hand-edit — changes will be overwritten.',
  ' */',
  '',
];

let extracted = 0;
const missingArtifacts = [];

for (const { sol, name } of MISSING) {
  const contractName = sol.replace(/\.sol$/, '');
  const artifactPath = join(ARTIFACT_DIR, sol, `${contractName}.json`);
  if (!existsSync(artifactPath)) {
    missingArtifacts.push(sol);
    continue;
  }
  const artifact = JSON.parse(readFileSync(artifactPath, 'utf8'));
  if (!artifact.abi || !Array.isArray(artifact.abi)) {
    console.warn(`No .abi in ${artifactPath}`);
    continue;
  }
  // Strip constructor + receive/fallback for cleaner hook generation
  const usefulAbi = artifact.abi.filter((x) => x.type !== 'constructor');
  lines.push(`export const ${name} = ${JSON.stringify(usefulAbi, null, 2)} as const;`);
  lines.push('');
  extracted++;
}

writeFileSync(OUTPUT_FILE, lines.join('\n'), 'utf8');

console.log(`\nWrote ${extracted}/${MISSING.length} ABIs to ${OUTPUT_FILE}`);
if (missingArtifacts.length) {
  console.warn('\nArtifacts not found for:');
  missingArtifacts.forEach((s) => console.warn(`  - ${s}`));
  console.warn('\nIf these contracts exist, they may not have been compiled by forge build.');
}

console.log('\nNext step: add this line to frontend/src/lib/contracts.ts:');
console.log('  export * from \'./abi-supplement\';');
